function suma(numero1, numero2){
    numero1= parseInt(prompt("Ingrese su primer numero (1/2): "))
    numero2= parseInt(prompt("Ingrese su segundo numero (2/2): "))
    var resultadoSuma= numero1 + numero2;
    alert("el resultado es "+ resultadoSuma)
}
